package com.project.dto;

public class RoomTypeVO {

	private int roomTypeSeq;
	private int hotelSeq;
	private String roomType;
	private int roomSize;	
	private String roomImage;
	private int personNumber;	
	private int roomPrice;
	private int roomResult;
	
	
	public int getRoomTypeSeq() {
		return roomTypeSeq;
	}
	public void setRoomTypeSeq(int roomTypeSeq) {
		this.roomTypeSeq = roomTypeSeq;
	}
	public int getHotelSeq() {
		return hotelSeq;
	}
	public void setHotelSeq(int hotelSeq) {
		this.hotelSeq = hotelSeq;
	}
	public String getRoomType() {
		return roomType;
	}
	public void setRoomType(String roomType) {
		this.roomType = roomType;
	}
	public int getRoomSize() {
		return roomSize;
	}
	public void setRoomSize(int roomSize) {
		this.roomSize = roomSize;
	}
	public String getRoomImage() {
		return roomImage;
	}
	public void setRoomImage(String roomImage) {
		this.roomImage = roomImage;
	}
	public int getPersonNumber() {
		return personNumber;
	}
	public void setPersonNumber(int personNumber) {
		this.personNumber = personNumber;
	}
	public int getRoomPrice() {
		return roomPrice;
	}
	public void setRoomPrice(int roomPrice) {
		this.roomPrice = roomPrice;
	}
	public int getRoomResult() {
		return roomResult;
	}
	public void setRoomResult(int roomResult) {
		this.roomResult = roomResult;
	}
	
	
	
}
